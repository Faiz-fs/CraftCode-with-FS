//switch
var fruits="kiwi";
//apple, orange,pineapple,banana
switch (fruits) {
    case "orange":
        console.log("i took orange");
        break;
    case "pineapple":
        console.log("i took pineapple");
        break;
    case "apple":
        console.log("i took apple");
        break;
    case "banana":
        console.log("i took banana");
        break;

    default:
        console.log("no fruit is not available");
        break;
}